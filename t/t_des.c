/***************************************************************************
 * Test functions
 */

static int
t_des_enc(char **desc, void *arg)
{
	struct t_case *t = arg;
	uint8_t out[DES_BLOCK_LEN];
	des_ctx ctx;
	size_t len;
	int ret;

	(void)asprintf(desc, "%s (encrypt)", t->desc);
	des_init(&ctx, CIPHER_MODE_ENCRYPT, t->key, sizeof t->key);
	len = des_encrypt(&ctx, t->ptext, out, DES_BLOCK_LEN);
	des_finish(&ctx);
	ret = t_compare_sz(DES_BLOCK_LEN, len) &
	    t_compare_mem(t->ctext, out, DES_BLOCK_LEN);
	return (ret);
}

static int
t_des_dec(char **desc, void *arg)
{
	struct t_case *t = arg;
	uint8_t out[DES_BLOCK_LEN];
	des_ctx ctx;
	size_t len;
	int ret;

	(void)asprintf(desc, "%s (decrypt)", t->desc);
	des_init(&ctx, CIPHER_MODE_DECRYPT, t->key, sizeof t->key);
	len = des_decrypt(&ctx, t->ctext, out, DES_BLOCK_LEN);
	des_finish(&ctx);
	ret = t_compare_sz(DES_BLOCK_LEN, len) &
	    t_compare_mem(t->ptext, out, DES_BLOCK_LEN);
	return (ret);
}


